package com.joyin.ticm.log.model;

import java.util.Date;

import com.joyin.ticm.bean.BaseBean;

/**
 * 日志实体类
 * 
 * @author colin
 * @version 1.0
 * @since Jul 18, 2013, 10:17:45 AM
 */
public class Log extends BaseBean {
	/** {field's description} */
	private static final long serialVersionUID = 76230000L;

	// Fields

	private long logid;
	private String logtype;
	private String userid;
	private String username;
	private Date createtime;
	private String clientip;
	private String delflg;
	private String logcontent;
	private Date lstmntdate;
	private String lstmntuser;

	// Constructors

	/** default constructor */
	public Log() {
	}

	/** full constructor */
	public Log(String logtype, String userid, String username, Date createtime,
			String clientip, String delflg, String logcontent, Date lstmntdate,
			String lstmntuser) {
		this.logtype = logtype;
		this.userid = userid;
		this.username = username;
		this.createtime = createtime;
		this.clientip = clientip;
		this.delflg = delflg;
		this.logcontent = logcontent;
		this.lstmntdate = lstmntdate;
		this.lstmntuser = lstmntuser;
	}

	// Property accessors

	public long getLogid() {
		return this.logid;
	}

	public void setLogid(long logid) {
		this.logid = logid;
	}

	public String getLogtype() {
		return this.logtype;
	}

	public void setLogtype(String logtype) {
		this.logtype = logtype;
	}

	public String getUserid() {
		return this.userid;
	}

	public void setUserid(String userid) {
		this.userid = userid;
	}

	public String getUsername() {
		return this.username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public Date getCreatetime() {
		return this.createtime;
	}

	public void setCreatetime(Date createtime) {
		this.createtime = createtime;
	}

	public String getClientip() {
		return this.clientip;
	}

	public void setClientip(String clientip) {
		this.clientip = clientip;
	}

	public String getDelflg() {
		return this.delflg;
	}

	public void setDelflg(String delflg) {
		this.delflg = delflg;
	}

	public String getLogcontent() {
		return this.logcontent;
	}

	public void setLogcontent(String logcontent) {
		this.logcontent = logcontent;
	}

	public Date getLstmntdate() {
		return this.lstmntdate;
	}

	public void setLstmntdate(Date lstmntdate) {
		this.lstmntdate = lstmntdate;
	}

	public String getLstmntuser() {
		return this.lstmntuser;
	}

	public void setLstmntuser(String lstmntuser) {
		this.lstmntuser = lstmntuser;
	}

}
